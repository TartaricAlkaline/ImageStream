   static final EnumMap<CommonData.Quant, EnumMap<CommonData.Comp, QuantLookup>> lookup = new EnumMap<>(CommonData.Quant.class);

   private QuantCommon() {
   }

   static short dc_lookup(short QIndex, short Delta) {
      return dc_qlookup[CUtils.clamp((short)(QIndex + Delta), (short)0, (short)127)];
   }

   static short ac_lookup(short QIndex, short Delta) {
      return ac_qlookup[CUtils.clamp((short)(QIndex + Delta), (short)0, (short)127)];
   }

   static short doLookup(CommonData cm, CommonData.Quant q, CommonData.Comp c, short qindex) {
      return doLookup(q, c, qindex, cm.delta_q.get(q).get(c));
   }

   static short doLookup(CommonData.Quant q, CommonData.Comp c, short qindex, short deltaQ) {
      return lookup.get(q).get(c).call(qindex, deltaQ);
   }

   static {
      EnumMap<CommonData.Comp, QuantLookup> temp = new EnumMap<>(CommonData.Comp.class);
      temp.put(CommonData.Comp.DC, QuantCommon::dc_lookup);
      temp.put(CommonData.Comp.AC, (qindex, delta) -> ac_lookup(qindex, (short)0));
      lookup.put(CommonData.Quant.Y1, temp);
      temp = new EnumMap<>(CommonData.Comp.class);
      temp.put(CommonData.Comp.DC, (qindex, delta) -> (short)(dc_lookup(qindex, delta) << 1));
      temp.put(CommonData.Comp.AC, (qindex, delta) -> (short)Math.max(8, ac_lookup(qindex, delta) * 101581 >> 16));
      lookup.put(CommonData.Quant.Y2, temp);
      temp = new EnumMap<>(CommonData.Comp.class);
      temp.put(CommonData.Comp.DC, (qindex, delta) -> CUtils.clamp(dc_lookup(qindex, delta), (short)0, (short)132));
      temp.put(CommonData.Comp.AC, QuantCommon::ac_lookup);
      lookup.put(CommonData.Quant.UV, temp);
   }

   interface QuantLookup {
      short call(short var1, short var2);
   }
