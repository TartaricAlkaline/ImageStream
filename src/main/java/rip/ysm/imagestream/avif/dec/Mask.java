   static int GetMaskValue(int[] mask, int maskPos, int[] maskNextRow, int mask_next_rowPos, int x, int subsamplingX, int subsamplingY) {
      if ((subsamplingX | subsamplingY) == 0) {
         return mask[x];
      } else {
         return subsamplingX == 1 && subsamplingY == 0
            ? D.RightShiftWithRounding(mask[maskPos + x * 2] + mask[maskPos + x * 2 + 1], 1)
            : D.RightShiftWithRounding(
               mask[maskPos + x * 2] + mask[maskPos + x * 2 + 1] + maskNextRow[mask_next_rowPos + x * 2] + maskNextRow[mask_next_rowPos + x * 2 + 1], 2
            );
      }
   }

   static void MaskBlend(
      int[] pred0,
      int pred0Pos,
      int[] pred1,
      int pred1Pos,
      int predictionStride1,
      int[] mask,
      int maskPos,
      int maskStride,
      int width,
      int height,
      int[] dst,
      int dstPos,
      int dstStride,
      boolean isInterIntra,
      int subsamplingX,
      int subsamplingY
   ) {
      int step_y = subsamplingY != 0 ? 2 : 1;
      int[] mask_next_row = mask;
      int mask_next_rowPos = maskPos + maskStride;
      int inter_post_round_bits = 4;

      for (int y = 0; y < height; y++) {
         for (int x = 0; x < width; x++) {
            int mask_value = GetMaskValue(mask, maskPos, mask_next_row, mask_next_rowPos, x, subsamplingX, subsamplingY);
            if (isInterIntra) {
               dst[dstPos + x] = D.RightShiftWithRounding(mask_value * pred1[pred1Pos + x] + (64 - mask_value) * pred0[pred0Pos + x], 6);
            } else {
               int res = mask_value * pred0[pred0Pos + x] + (64 - mask_value) * pred1[pred1Pos + x] >> 6;
               res += 0;
               dst[dstPos + x] = D.Clip3(D.RightShiftWithRounding(res, 4), 0, 255);
            }
         }

         dstPos += dstStride;
         maskPos += maskStride * step_y;
         mask_next_rowPos += maskStride * step_y;
         pred0Pos += width;
         pred1Pos += predictionStride1;
      }
   }

   static void InterIntraMaskBlend8bpp(
      int[] pred0,
      int pred0Pos,
      int[] pred1,
      int pred1Pos,
      int prediction_stride_1,
      int[] mask,
      int maskPos,
      int mask_stride,
      int width,
      int height,
      int subsampling_x,
      int subsampling_y
   ) {
      int step_y = subsampling_y != 0 ? 2 : 1;
      int[] mask_next_row = mask;
      int mask_next_rowPos = maskPos + mask_stride;

      for (int y = 0; y < height; y++) {
         for (int x = 0; x < width; x++) {
            int mask_value = GetMaskValue(mask, maskPos, mask_next_row, mask_next_rowPos, x, subsampling_x, subsampling_y);
            pred1[pred1Pos + x] = D.RightShiftWithRounding(mask_value * pred1[pred1Pos + x] + (64 - mask_value) * pred0[pred0Pos + x], 6);
         }

         maskPos += mask_stride * step_y;
         mask_next_rowPos += mask_stride * step_y;
         pred0Pos += width;
         pred1Pos += prediction_stride_1;
      }
   }

